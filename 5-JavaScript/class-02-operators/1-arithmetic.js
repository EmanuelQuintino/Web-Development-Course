// Arithmetic Operators

let num1 = "2";
let num2 = 2;

console.log(num1 + num2);
console.log(num1 - num2);
console.log(num1 * num2);
console.log(num1 / num2);
console.log(num1 % num2);
console.log(num1 ** num2);
console.log(16 ** (1 / 2));
console.log(Math.sqrt(16));
console.log(Math.cbrt(8));
console.log(Math.pow(2, 4));

// increment ( ++ )
console.log(num1++); // pos
console.log(num1);

// decrement ( -- )
console.log(--num1); // pre
console.log(num1);

console.log(++num1 + num2);

// Assignment (Atribuição)

num1 = num1 + 2;

num1 += 2;
num1 -= 2;
num1 *= 2;
num1 /= 2;
num1 %= 2;
num1 **= 2;

console.log(num1);
