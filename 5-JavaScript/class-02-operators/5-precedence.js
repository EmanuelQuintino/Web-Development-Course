// Operator Order Precedence

console.log(7 + 8 / 2);
console.log((7 + 8) / 2);
console.log(3 > 2 && 4 < 10);
console.log(3 < 2 || 4 < 10);
console.log(6 < 2 || 4 > 10);
console.log(3 > 2 > 1);
console.log(true == 1);
console.log(false == 0);
console.log(3 > 2 == 1);
console.log(true === 1);
console.log(3 > 2 && 2 > 1);
